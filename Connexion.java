    private void runn(){
        
            loader.show();
            login.hide();
            
           
            
            
            String mail = txt_email.getText();
            String pass = txt_pwd.getText();
            
            String cqlStatement = "SELECT * FROM users WHERE email = '"
                    + mail + "' and mdp = '" + pass + "' allow filtering ;";
            
            
            
            
            try{
                final Row row = session.execute(cqlStatement).one();
                mailBD  = row.getString(1);
                passBD = row.getString(2);
                typecompte = row.getString(7);
                id = row.getString(0);
                
                for (Row n : session.execute(cqlStatement)) {
                    i++;
                }
                
            } catch (Exception e) {
                System.out.print("err");
            }
            
            
            
            
             
           
        new java.util.Timer().schedule(new TimerTask() {
            
            @Override
            public void run() {
                
                
                if(i == 1)
                {
                  if(mailBD.equals(mail) && passBD.equals(pass) && typecompte.equalsIgnoreCase("Medcin")){
                    
                    System.out.println("ani hna");
                    new HomeMedecin(id).setVisible(true);
                    //m.show();
                    dispose();
                }
                  
                  if (mailBD.equals(mail) && passBD.equals(pass) && typecompte.equalsIgnoreCase("admin")){
                        System.out.println("ani hna f admin");
                        new Homeadmin(id).setVisible(true);
                        //m.show();
                        dispose();
                     }
                  if (mailBD.equals(mail) && passBD.equals(pass) && typecompte.equalsIgnoreCase("cons")){
                        System.out.println("ani hna f Home Consultant");
                        HomeConsultant m = new HomeConsultant();
                        //m.show();
                        dispose();
                     } 
                }
                
                else {
                    Connexion m = new Connexion(false);
                    System.out.println(".run()"+txt_email.getText());
                    m.txt_email.setText(txt_email.getText());
                    m.err.setText("Votre Email Ou Votre Mot de passe Incorrect");
                    m.show();
                    dispose();
                }
                
                
            }
        },1000*3);
    }
    
    
    
   
    
